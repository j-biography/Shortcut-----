# Ctrl + K + R = 현재 파일 탐색기로 열기
# Ctrl + K + O = 현재 파일 새 창에 열기


#       * F11 = 전체화면 전환 *


# Shift + Alt + 0 = 편집기 레이아웃 설정, 해제
# Ctrl + =, - = 보기 확대, 축소
# Ctrl + B = 사이드바 표시, 숨김
# Ctrl + Shift + E = 탐색기 표시, 포커스 전환
# Ctrl + Shift + F = 검색 탭 보기
# Ctrl + Shift + G = 소스 제어 탭 보기
# Ctrl + Shift + D = 디버그 탭 보기 
# Ctrl + Shift + X = 확장 탭 보기
# Ctrl + Shift + H = 찾아바꾸기 탭 보기  
# Ctrl + Shift + J = 검색에서 포함 파일, 제외 파일 옵션 탭 보기 
# Ctrl + Shift + U = 출력 탭 보기 
# Ctrl + Shift + V = 마크다운 미리보기 탭 보기
# Ctrl + K + V = 우측화면 마크다운 미리보기 탭 보기
# Ctrl + K + Z = 젠 모드(Esc로 모드를 해제)


#       * F9 = 중단점 설정 토글 *
#       * F5 = 디버깅 시작 *
#       * Shift + F5 = 디버깅 중지 *


# F11 or Shift + F11 = 단계 정보, 단계 출력
# F10 = 디버그 단위 실행
# Ctrl + K + Ctrl + I = 디버그 가리키기 표시


