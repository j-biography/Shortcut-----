
# VSCode shortcut compilation

# Ctrl + Shift + P or F1 = 명령팔레트 열기
# Ctrl + P = 다른 파일로 이동, 빠른 파일 열기
# Ctrl + Shift + N = 새 창 열기
# Ctrl + Shift + W = 창 닫기
# Ctrl + , = 사용자 설정 열기
# Ctrl + K + Ctrl + S = 단축키 목록 보기(순서대로 4개의 키를 눌러야 함)
# Ctrl + K + Ctrl + R = 단축키 목록을 PDF 파일로 보기(순서대로 4개의 키를 눌러야 함)

# Ctrl + X = 현재 줄 잘라내기
# Ctrl + C = 현재 줄 복사하기
# Ctrl + V = 복사한 줄 붙여넣기


#       * Alt + 화살표(위아래) = 현재 줄 위아래로 이동시키기 *
#       * Shift + Alt + 화살표(위아래) = 현재 줄 위아래로 복사시키기 *


# Ctrl + Shift + K = 현재 줄 삭제
# Ctrl + Enter = 아래에 줄 삽입
# Ctrl + Shift + Enter = 위에 줄 삽입
# Ctrl + Shift + \ = 괄호로 이동
# Ctrl + [ ] = 들여쓰기, 내어쓰기


#       * Home or End = 줄의 시작과 끝 부분으로 커서 이동 *
#       * Ctrl + Home = 파일의 시작 위치로 커서 이동 *
#       * Ctrl + End = 파일의 끝 위치로 커서 이동 *


# Ctrl + 화살표(위아래) = 스크롤 위아래 이동
# Alt + Page Up or Page Down = 스크롤 페이지 위아래 이동
# Ctrl + Shift + [ ] = 블록 접기, 펴기
# Ctrl + K + Ctrl + 0 = 모든 블록 접기
# Ctrl + K + Ctrl + J = 모든 블록 펴기
# Ctrl + / =  줄 주석 처리, 제거(토글)
# Ctrl + K + Ctrl + C = 줄 주석 처리
# Ctrl + K + Ctrl + U = 줄 주석 제거
# Shift + Alt + A = 블록 주석 처리, 제거(토글)
# Alt + Z = 자동 줄바꿈(토글)

# Ctrl + T = 모든 기호 표시
# Ctrl + G = 특정 행번호로 이동
# Ctrl + Shift + O = 기호로 이동
# Ctrl + Shift + M = 문제 패널 보기(오류, 경고, 정보)
# F8 = 다음 문제로 이동
# Shift + F8 = 이전 문제로 이동
# Ctrl + Shift + Tab = 네비게이터 히스토리 이동
# Alt + 화살표(좌측, 우측) = 뒤로 가기, 앞으로 가기
# Ctrl + M = Tab키로 포커스 이동(토글)


#       * Ctrl + F = 찾기 *
#       * Ctrl + H = 찾고 바꾸기 *


# F3 or Shift + F3 = 찾기 다음, 이전
# Alt + Enter = 검색편집기
# Ctrl + D = 다음 일치 항목을 선택 항목으로 추가
# Ctrl + K + Ctrl + D = 다음 일치 항목의 마지막 선택항목으로 이동
# Alt + C, R, W = 찾기 옵션 대소문자, 정규식, 단어 설정(토글)

# Alt + 클릭 = 커서 추가
# Ctrl + Alt + 화살표(위아래) = 커서 위, 아래에 추가
# Ctrl + U = 마지막 커서 작업 실행 취소
# Shift + Alt + I = 선택한 각 줄 끝의 커서 삽입
# Ctrl + L = 현재 줄 선택
# Ctrl + Shift + L = 일치 항목 모두 선택하여 커서 추가
# Ctrl + F2 = 모든 항목 변경
# Shift + Alt + 화살표(우측, 좌측) = 선택 영역 확장, 축소

# Shift + Alt + 마우스 드래그 = 마우스로 영역 선택해서 커서 추가
# Ctrl + Shift + Alt + 화살표 = 키보드로 영역 선택해서 커서 추가
# Ctrl + Shift + Alt + PgUp, PgDn = 페이지 단위로 영역 선택해서 커서 추가

# Ctrl + SpaceBar = 코드 힌트
# Ctrl + Shift + SpaceBar = 함수(메서드) 제안
# Shift + Alt + F = 문서 서식 지정
# Ctrl + K + Ctrl + F = 선택 영역 서식 지정


#       * F12 = 코드 정의 보기 *
#       * Alt + F12 = 코드 정의를 현재 창에서 띄움 *


# Ctrl + K + F12 = 코드 정의를 측면에 띄움
# Ctrl + . = 빠른 수정
# Shift + F12 = 참조로 이동
# F2 = 기호 이름 바꾸기
# Ctrl + K + Ctrl + X = 후행 공백 자르기
# Ctrl + K + M = 언어 모드 변경

# Ctrl + F4 or Ctrl + W = 현재 편집중인 화면 닫기
# Ctrl + K + F = 현재 편집중인 폴더 닫기
# Ctrl + \ = 화면 분할
# Ctrl + 1, 2, 3 = 분할한 화면 이동
# Ctrl + K + Ctrl + 화살표(좌측, 우측) = 분할한 화면 이동 이전, 다음
# Ctrl + Shift + PgUp, PgDn = 편집 화면 탭 이동
# Ctrl + K + 화살표(좌측, 우측) = 편집 그룹 이동


#       * Ctrl + N = 새 파일을 새 탭에 열기 *
#       * Ctrl + O = 특정 파일 열기 *
#       * Ctrl + S = 파일 저장 *
#       * Ctrl + Shift + S = 새이름으로 파일 저장 *
#       * Ctrl + K + S = 모두 저장하기 *
#       * Ctrl + F4 = 파일 닫기 *


